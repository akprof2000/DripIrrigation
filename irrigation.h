// irrigation.h 🌱 Доменная логика полива (функциональное ядро, без I/O)
// Здесь только ЧИСТЫЕ решения: по входным данным (влажность, калибровка канала,
// дельта, предыдущее состояние) вычисляем, что делать с клапаном и нужно ли
// уведомлять. Никакого железа, SD и Telegram — модуль тестируется на хосте.
#pragma once

#include <Arduino.h>

// 🚰 Желаемое действие над клапаном
enum class ValveAction : uint8_t {
  Open,   // открыть
  Close,  // закрыть
  Hold    // ничего не делать (зона гистерезиса)
};

// 📋 Результат решения по одному каналу
struct ValveDecision {
  ValveAction action;   // что сделать с клапаном
  uint8_t     newState; // новое состояние (VState::*) для отслеживания/уведомления
  bool        notify;   // нужно ли отправить уведомление об изменении
};

// 🧠 Чистая функция решения по каналу.
//   mode      — режим канала (Mode::*)
//   humidity  — текущая влажность, %
//   border    — порог срабатывания, %
//   deltaHum  — половина гистерезиса, %
//   prevState — предыдущее состояние (VState::*)
ValveDecision decideValve(uint8_t mode, int humidity, int border,
                          int deltaHum, uint8_t prevState);
