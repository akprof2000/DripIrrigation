// 🧽 Контроль засора фильтра по скорости потока
// ⏳ Адаптивное ожидание выхода на режим после открытия клапана: при открытии
// пустая труба наполняется и поток кратковременно завышен (гидроудар/наполнение),
// потом падает до реального. На дальних ветках спад плавный (доли % за 20 с),
// поэтому детектор «перестал падать между соседними подынтервалами» может
// сработать ложно, пока труба ещё наполняется — отсюда жёсткий минимум
// FM_SETTLE_MIN_MS: раньше него «стабилизация» вообще не проверяется (по
// наблюдению — первые 10 минут после открытия клапана поток гарантированно
// ещё падает). После минимума делим время на подынтервалы (FM_SUBSAMPLE_MS) и
// ждём, пока скорость перестанет заметно падать между соседними подынтервалами
// (FM_SETTLE_STABLE_PCT, FM_STABLE_SAMPLES_NEEDED раз подряд) — тогда и
// начинаем настоящее окно замера. FM_SETTLE_CAP_MS — общий потолок ожидания на
// случай, если стабилизация не наступит вовсе: не ждём бесконечно, начинаем
// замер тем, что есть.
#define FM_SETTLE_MIN_MS 600000         // ⏱️ минимум ожидания (10 мин) — раньше стабилизация не засчитывается
#define FM_SUBSAMPLE_MS 20000           // ⏱️ длительность подынтервала при ожидании стабилизации потока
#define FM_SETTLE_STABLE_PCT 10         // 📉 если поток упал меньше чем на N% к пред. подынтервалу — считаем стабильным
#define FM_STABLE_SAMPLES_NEEDED 2      // 🔁 сколько стабильных подынтервалов подряд нужно для подтверждения
#define FM_SETTLE_CAP_MS 900000         // ⏱️ потолок ожидания стабилизации (15 мин) — дальше замер начнётся принудительно
#define FM_WINDOW_MS 30000             // ⏱️ длительность окна замера скорости потока
#define FM_MIN_PULSES 40               // 📟 минимум импульсов в окне для значимого замера
#define FM_BAD_WINDOWS 3               // 🔁 сколько «плохих» окон подряд подтверждают засор
#define FM_ALERT_REPEAT_MS 86400000UL  // ⏱️ повтор тревоги не чаще раза в сутки

// 📈 Разовый выброс скорости потока (пузырь воздуха, наводка на датчике) не должен
// сразу становиться новым эталоном «на клапан». Повышенное значение принимается
// кандидатом; пока держится непрерывно FM_SPIKE_CONFIRM_MS, фиксируется МИНИМУМ
// из всех замеров этого периода (колебания внутри всплеска в пределах
// FM_SPIKE_TOLERANCE_L не сбрасывают кандидата) — минимум и становится эталоном.
// Откат больше допуска — кандидат сбрасывается, выброс не подтвердился.
#define FM_SPIKE_CONFIRM_MS 300000     // ⏱️ время подтверждения повышенного эталона (5 мин)
#define FM_SPIKE_TOLERANCE_L 1.0       // 📏 допустимый откат от минимума кандидата, л/мин
