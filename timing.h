#define CHECK_WIFI_INTERVAL 30000      // ⏱️ мс — основной интервал проверки WiFi
#define CHECK_INTERVAL 10000           // ⏱️ мс — интервал цикла полива
#define CHECK_LIGHT true
#define CHECK_RAIN true
#define FILLING_WAIT 3000              // ⏱️ мс — время наполнения бака
#define TIMEOUT_WAIT 18000             // ⏱️ с — таймаут ожидания закрытия клапанов
#define DRAIN_TIMEOUT 12000            // ⏱️ мс — время пролива дренажа
#define PUMP_TIMEOUT 30                // ⏱️ с — максимальное время работы насоса
#define NTP_SYNC_INTERVAL 3600000UL    // ⏱️ мс — период принудительной синхронизации NTP (1 час)
#define TG_TIMEOUT_DEFAULT 150         // ⏱️ с — таймаут потери связи с Telegram по умолчанию (2.5× long-poll); хранится в Config

// 🧽 Контроль засора фильтра по скорости потока
// ⏳ Адаптивное ожидание выхода на режим после открытия клапана: при открытии
// пустая труба наполняется и поток кратковременно завышен (гидроудар/наполнение),
// потом падает до реального. Вместо фиксированной паузы делим время на короткие
// подынтервалы (FM_SUBSAMPLE_MS) и ждём, пока скорость перестанет заметно падать
// между соседними подынтервалами (FM_SETTLE_STABLE_PCT, FM_STABLE_SAMPLES_NEEDED
// раз подряд) — тогда и начинаем настоящее окно замера. FM_SETTLE_CAP_MS — потолок
// ожидания на случай, если стабилизация не наступит (труба очень длинная и т.п.):
// не ждём вечно, начинаем замер тем, что есть.
#define FM_SUBSAMPLE_MS 20000           // ⏱️ длительность подынтервала при ожидании стабилизации потока
#define FM_SETTLE_STABLE_PCT 10         // 📉 если поток упал меньше чем на N% к пред. подынтервалу — считаем стабильным
#define FM_STABLE_SAMPLES_NEEDED 2      // 🔁 сколько стабильных подынтервалов подряд нужно для подтверждения
#define FM_SETTLE_CAP_MS 180000         // ⏱️ потолок ожидания стабилизации (3 мин) — дальше замер начнётся принудительно
#define FM_WINDOW_MS 30000             // ⏱️ длительность окна замера скорости потока
#define FM_MIN_PULSES 40               // 📟 минимум импульсов в окне для значимого замера
#define FM_BAD_WINDOWS 3               // 🔁 сколько «плохих» окон подряд подтверждают засор
#define FM_ALERT_REPEAT_MS 86400000UL  // ⏱️ повтор тревоги не чаще раза в сутки
